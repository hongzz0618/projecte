//Convertir POsicion a letra
function posicionTostring(letra){
    switch (letra) {
        case 'G':return 'Base';
        break;
        case 'F' :return 'Alero';
        break;
        case 'C':return 'Pivot'
        break;
        case 'G-F' : return 'Base-Alero'
        break;
        case 'C-G' :return 'Pivot-Base'
        break;
        case 'F-C':
        return 'Alero-Pivot'
        break;
        case 'G-C': return 'Base-Pivot'
         break;
        case 'F-G': return 'Alero-Base'
         break;
        case 'C-F': return 'Pivot-Alero'
         break;
        case 'G-F-C': return 'Base-Alero-Pivot'
         break;
        case 'G-C-F': return 'Base-Pivot-Alero'
         break;
        case 'C-F-G': return 'Pivot-Alero-Base'
         break;
        case 'C-G-F': return 'Pivot-Base-Alero'
         break;
        case 'F-C-G' :return 'Alero-Pivot-Base'
        break;
        case 'F-G-C' :return 'Alero-Base-Pivot'
        break;
        case lower('G'): return 'Base'
        break;
        case lower('F'): return 'Alero'
        break;
        case lower('C'): return 'Pivot'
        break;
        case lower('G-F'): return 'Base-Alero'
        break;
        case lower('C-G'): return 'Pivot-Base'
        break;
        case lower('F-C'): return 'Alero-Pivot'
        break;
        case lower('G-C'): return 'Base-Pivot'
        break;
        case lower('F-G'): return 'Alero-Base'
        break;
        case lower('C-F'): return 'Pivot-Alero'
        break;
        case lower('G-F-C'): return 'Base-Alero-Pivot'
        break;
        case lower('G-C-F'): return 'Base-Pivot-Alero'
        break;
        case lower('C-F-G'): return 'Pivot-Alero-Base'
        break;
        case lower('C-G-F'): return 'Pivot-Base-Alero'
        break;
        case lower('F-C-G'): return 'Alero-Pivot-Base'
        break;
        case lower('F-G-C'): return 'Alero-Base-Pivot'
        break;
    }
}
//VALIDAR UNA FECHA DE INICIO CON LA FINAL QUE QUIERAS
function validate_fechaMayorQue(fechaInicial,fechaFinal){
    valuesStart=fechaInicial.split("/");
    valuesEnd=fechaFinal.split("/");
    // Verificamos que la fecha no sea posterior a la actual
    var dateStart=new Date(valuesStart[2],(valuesStart[1]-1),valuesStart[0]);
    var dateEnd=new Date(valuesEnd[2],(valuesEnd[1]-1),valuesEnd[0]);
    if(dateStart>=dateEnd)
    {
        return 0;
    }
    return 1;
}